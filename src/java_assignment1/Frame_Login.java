    public void rst() {
        usernametxt.setText(null);
        passwordtxt.setText(null);
        usertype.setSelectedItem("- Please Select -");
    }

    public final void showtime() {
        new Timer(0, (ActionEvent e) -> {
            Date date = new Date();
            SimpleDateFormat s = new SimpleDateFormat("hh:mm:ss a");
            timelbl.setText(s.format(date));
        }).start();
    }

    public void verify() {
        String cmbobox = usertype.getSelectedItem().toString();
        if (usernametxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Username Field is empty");
        } else if (passwordtxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Password Field is empty");
        } else if (cmbobox.equals("- Please Select -")) {
            JOptionPane.showMessageDialog(null, "Please select the user type");
        } else {

        }
    }

    public final void list() {
        usertype.addItem("- Please Select -");
        usertype.addItem("Admin");
        usertype.addItem("Tutor");
        usertype.addItem("Student");
        usertype.setSelectedItem("- Please Select -");
    }


