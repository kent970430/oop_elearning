    private void lgnbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lgnbtnActionPerformed
        verify();
        String usnm = usernametxt.getText();
        String psww = String.valueOf(passwordtxt.getPassword());
        String cmbo = usertype.getSelectedItem().toString();
        Login lg = new Login();
        
        int flag = lg.loginn(usnm, psww, cmbo);
        switch (flag) {
            case 1:
                this.setVisible(false);
                JOptionPane.showMessageDialog(null, "Student Login Success!");
                Frame_Student fs = new Frame_Student();
                fs.setVisible(true);
                break;
            case 2:
                this.setVisible(false);
                JOptionPane.showMessageDialog(null, "Tutor Login Success!");
                Frame_Tutor ft = new Frame_Tutor();
                ft.setVisible(true);
                break;
            case 3:
                this.setVisible(false);
                JOptionPane.showMessageDialog(null, "Admin Login Success!");                
                Frame_Admin fa = new Frame_Admin();
                fa.setVisible(true);
                break;
            case 4:
                JOptionPane.showMessageDialog(null, "Login Failed, please try again!");
                rst();
                break;
        }

    }//GEN-LAST:event_lgnbtnActionPerformed

