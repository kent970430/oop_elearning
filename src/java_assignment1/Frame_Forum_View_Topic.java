    public String showUser() {
        Database database = new Database();
        database.getAllData();
        try {
            ArrayList<User> userList = database.getUser();
            int size = userList.size();
            for (int i = 0; i < size; i++) {
                this.userrr = userList.get(i).getUsername();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Profile Error, " + e);
        }
        return userrr;
    }

    public void showTable() {
        Database database = new Database();
        database.getAllData();;
        ArrayList<Comment> comment = database.getComment();
        DefaultTableModel model = (DefaultTableModel) commenttable.getModel();
        Object[] row = new Object[2];
        for (int i = 0; i < comment.size(); i++) {
            if (comment.get(i).getForumID().equals(forumidtxt.getText()) && comment.get(i).getTopic().equals(topictxt.getText())) {
                row[0] = comment.get(i).getComments();
                row[1] = comment.get(i).getUsers();
                model.addRow(row);
            }
        }
    }

