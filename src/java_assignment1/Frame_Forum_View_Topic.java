    private void commenttableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_commenttableMouseClicked
        String user = showUser();
        commenttxt.setText(null);
        Database database = new Database();
        database.getAllData();;
        ArrayList<Comment> comment = database.getComment();
        DefaultTableModel model = (DefaultTableModel) commenttable.getModel();
        int selectedRowIndex = commenttable.getSelectedRow();
        for (int i = 0; i < comment.size(); i++) {
            if (comment.get(i).getUsers().equals(user) && comment.get(i).getComments().equals(model.getValueAt(selectedRowIndex, 0).toString())) {
                //System.out.println(comment.get(i).getUsers());              
                commenttxt.setText(model.getValueAt(selectedRowIndex, 0).toString());              
            }
        }
        //commenttxt.setText(null);

    }//GEN-LAST:event_commenttableMouseClicked

