    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        String course = coursetxt.getText();
        String sub = subtxt.getText();
        String forumidd = forumidtxt.getText();
        String topicc = topictxt.getText();
        String des = descriptiontxt.getText();
        String commentt = commenttxt.getText();
        String user = showUser();
        Comment cc = new Comment();
        int count = cc.delete(course, sub, forumidd, topicc, des, user, commentt);
        switch (count) {
            case 1:
                //JOptionPane.showMessageDialog(null, "Forum Added!");
                commenttxt.setText(null);
                DefaultTableModel dm = (DefaultTableModel) commenttable.getModel();
                while (dm.getRowCount() > 0) {
                    dm.removeRow(0);
                }
                showTable();
                break;
            case 2:
                JOptionPane.showMessageDialog(null, "Delete fail, please try again");
                break;
            default:
                break;
        }
    }//GEN-LAST:event_deletebtnActionPerformed

