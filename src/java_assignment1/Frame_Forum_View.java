    public void list() {
        coursecmb.addItem("- Please Select -");
        coursecmb.setSelectedItem("- Please Select -");

        subcmb.addItem("- Please Select -");
        subcmb.addItem("Subject Topics");
        subcmb.addItem("Helpful Resources");
        subcmb.addItem("General Discussion");
        subcmb.setSelectedItem("- Please Select -");

        Database database = new Database();
        database.getAllData();

        try {
            ArrayList<Course> courseList = database.getCourse();
            int size = courseList.size();
            for (int i = 0; i < size; i++) {
                coursecmb.addItem(courseList.get(i).getCourseName());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Course list Error, " + e);
        }
    }

    public void showTable() {
        Database database = new Database();
        database.getAllData();;
        ArrayList<Forum> forumm = database.getForum();
        DefaultTableModel model = (DefaultTableModel) forumtable.getModel();
        Object[] row = new Object[3];
        for (int i = 0; i < forumm.size(); i++) {
            if (forumm.get(i).getCourse().equals(coursecmb.getSelectedItem().toString()) && forumm.get(i).getSubforum().equals(subcmb.getSelectedItem().toString())) {
                row[0] = forumm.get(i).getForumID();
                row[1] = forumm.get(i).getTopic();
                row[2] = forumm.get(i).getDiscussion();
                model.addRow(row);
            }
        }
    }

