    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Database database = new Database();
        database.getAllData();

        try {
            ArrayList<User> userList = database.getUser();

            for (int i = 0; i < userList.size(); i++) {
                String status = userList.get(i).getStatus();

                if (status.equals("student")) {
                    Frame_Student fs = new Frame_Student();
                    fs.setVisible(true);
                    this.setVisible(false);
                } else if (status.equals("tutor")) {
                    Frame_Tutor_Forum ftf = new Frame_Tutor_Forum();
                    ftf.setVisible(true);
                    this.setVisible(false);
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Profile Error, " + e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

